
name = "this is a string"

# this is a for loop
# notice that the character loops over the character,
# NOT the index of the string
for character in name:
    print(character)

# looping over a range of numbers
for number in range(0, 9):
    print(number)

# a while loop using += operators
counter = 0
while counter < 10:
    print(counter)
    counter += 1
